#!/usr/bin/env node
/* Author-time asset generator for UX-First Studio.
   Uses the EnsoKit brush engine to emit authentic sumi-e ensō marks as
   static SVGs (for CSS backgrounds) and an inline-path JS module (for
   animated, currentColor heading marks). The APP itself stays build-free;
   this runs once here and commits the results. Re-run: node build-enso-art.js */
const fs = require("fs");
const path = require("path");
const BK = require("../ensokit/src/brush.js");
const { ensoPoints, strokePoints, dotPoints, toPathD, svgWrap } = BK;

const OUT = path.join(__dirname, "art");
fs.mkdirSync(OUT, { recursive: true });

/* A full brushed ensō ring as an SVG path string on a 0..100 grid. */
function ensoPath(seed, opts) {
  return toPathD(ensoPoints(seed, opts));
}

/* Big faint watermark ensō — one per seed, baked to a fixed color+opacity,
   used as a CSS background-image behind station headers and panels. */
function watermarkSVG(seed, color, opacity, opts = {}) {
  const d = ensoPath(seed, Object.assign({ R: 38, w: 12, cx: 50, cy: 50 }, opts));
  const body = `<path d="${d}" fill="${color}" fill-opacity="${opacity}"/>`;
  return svgWrap(body, { size: 0, attrs: ' preserveAspectRatio="xMidYMid meet"' });
}

/* Vermillion hanko seal — a small square-ish double frame with a brushed
   ensō inside, the classic "red seal on a sumi drawing". */
function sealSVG(seed) {
  const V = "#D95B31";
  const ring = ensoPath(seed + "|seal", { R: 30, w: 9, cx: 50, cy: 50, gap: 0.5 });
  const dot = toPathD(dotPoints(seed + "|d", 50, 50, 6));
  const body =
    `<rect x="8" y="8" width="84" height="84" rx="10" fill="none" stroke="${V}" stroke-width="4"/>` +
    `<path d="${ring}" fill="${V}"/>` +
    `<path d="${dot}" fill="${V}"/>`;
  return svgWrap(body, { size: 0 });
}

/* Horizontal brush stroke — a tapered sumi swipe for heading underlines. */
function underlineSVG(seed, color) {
  const pts = strokePoints(seed + "|ul", [[4, 14], [30, 9], [64, 15], [96, 8]], 7, { taper: "both" });
  const body = `<path d="${toPathD(pts)}" fill="${color}"/>`;
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 100 22" preserveAspectRatio="none">${body}</svg>`;
}

/* ---- emit static SVGs referenced by CSS ----
   (Station watermarks, brand mark, hero and progress rings are injected inline
   from enso-art.js below, so only these three static assets are needed.) */
const files = {
  "enso-seal.svg": sealSVG("uxfs"),                          // #sideSeal <img>
  "brush-underline.svg": underlineSVG("uxfs-ul", "#1f44e0"), // .st-title::after
  "brush-underline-ink.svg": underlineSVG("uxfs-ul", "#16161a"), // .learn h3::before mask
};
// watermarkSVG kept above for anyone wanting raster/static fallbacks; unused here.
void watermarkSVG;
for (const [name, svg] of Object.entries(files)) {
  fs.writeFileSync(path.join(OUT, name), svg);
}

/* ---- emit inline-path JS module for the engine (currentColor + animation) ----
   Each entry is a raw <path d> string on a 0 0 100 100 viewBox, tinted at
   render time via CSS `fill:currentColor`, so it inherits ink/accent/vermillion. */
const inline = {
  // 8 station ring variants, each a single brushed ensō contour
  rings: Array.from({ length: 8 }, (_, i) =>
    ensoPath("uxfs-inline-" + i, { R: 40, w: 12, cx: 50, cy: 50, rot: -1.25 + i * 0.5, gap: 0.5 + (i % 3) * 0.2 })),
  // a compact ensō for the brand mark / small accents
  mark: ensoPath("uxfs-mark", { R: 38, w: 13, cx: 50, cy: 50, gap: 0.55, rot: -1.3 }),
  // a near-complete brushed ring for the progress indicator — starts drawing
  // at the top so a clockwise conic reveal reads as "inking the circle closed"
  progress: ensoPath("uxfs-progress", { R: 41, w: 9, cx: 50, cy: 50, gap: 0.34, rot: -Math.PI / 2 + 0.05 }),
};
const js =
  "/* GENERATED by build-enso-art.js — authentic EnsoKit brush geometry.\n" +
  "   Do not hand-edit; re-run `node build-enso-art.js` to regenerate. */\n" +
  "var ENSO_ART = " + JSON.stringify(inline, null, 0) + ";\n";
fs.writeFileSync(path.join(__dirname, "enso-art.js"), js);

console.log("Wrote", Object.keys(files).length, "SVGs to art/ and enso-art.js");
console.log("Files:", Object.keys(files).join(", "));
